-- Relasi merupakan kumpulan relasi yang saling terhubung satu sama lain

-- Record adalah satu baris table, row atau tuple

-- Field adalaha istilah untuk menunjukan satu data didalam table

-- Candidate key adalah satu atau beberapa kolom didalam table yang bisa mengidentifikasi baris dari table tersebut
-- selain tidak boleh ada data yang sama kolom yang didefinisikan dengan candidate key tidak boleh kosong

-- Primary key adalah kolom yang akan menjadi identitas pada setiap baris table

-- Foreign key adalah primary key dari table lain yang berada pada table saat ini

--Referential integrity adalah penerapan aturan bahwa untuk setiap foreigh key yang terdapat pada suatu table
-- harus ada nilainya di table asal kolom tersebut.

-- Normalisasi database adalah proses penyusunan kolom dan table untuk mengurangi redudansi data dengan tujuan untuk 
-- memecah table yang besar menjadi table yang kecil agar mudah terorganisir

--DDL(Data Definition Language) adlah jenis intruksi sql yang berkaitan dengan pembuatan struktur table dan database
-- seperti CREAT, DROP, ALTER DAN RENAME

-- DML (Data Manipulation Language) adalah jenis instruksi sql yang berkaitan dengan data yang ada dalam table
-- seperti SELECT, INSERT, DELETE dan UPDATE

-- Data Control Language (DCL) adalah jenis instruksi SQL yang berkaitan dengan manajemen hak akses dan pengguna (user). 
-- Perintah ini digunakan untuk membatasi siapa saja yang dapat mengakses database dan tabel. 
-- Perintah SQL yang termasuk ke dalam kategoriDCL adalah GRANT dan REVOKE.

-- TCL adalah Perintah sql untuk melakukan proses transaksi 

